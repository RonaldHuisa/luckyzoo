const ptToEs = Object.fromEntries(Object.entries(esToPt).map(([es, pt]) => [pt, es]));
const zhToEs = Object.fromEntries(Object.entries(esToZh).map(([es, zh]) => [zh, es]));

function normalizeText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

export function translateText(value, language) {
  const text = normalizeText(value);
  if (!text) return value;

  // Primero resolvemos el texto a su base en español. Esto permite cambiar
  // ES → EN → PT → ZH sin romper números ni valores dinámicos.
  const baseEs = enToEs[text] || ptToEs[text] || zhToEs[text] || text;

  if (language === "es") return baseEs;
  if (language === "en") return esToEn[baseEs] || value;
  if (language === "pt") return esToPt[baseEs] || esToEn[baseEs] || value;
  if (language === "zh") return esToZh[baseEs] || esToEn[baseEs] || value;

  return value;
}

const I18nContext = createContext({
  language: "es",
  setLanguage: () => {},
  t: (value) => value,
});

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(() => {
    const saved = localStorage.getItem(LANGUAGE_KEY);
    return LANGUAGES[saved] ? saved : "es";
  });

  const setLanguage = useCallback((nextLanguage) => {
    const normalized = LANGUAGES[nextLanguage] ? nextLanguage : "es";
    localStorage.setItem(LANGUAGE_KEY, normalized);
    document.documentElement.lang = normalized;
    setLanguageState(normalized);
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const t = useCallback((value) => translateText(value, language), [language]);

  const value = useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
