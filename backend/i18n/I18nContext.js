const enToEs = Object.fromEntries(Object.entries(esToEn).map(([es, en]) => [en, es]));

function normalizeText(value) {
  return String(value || "").replace(/\s+/g, " ").trim();
}

export function translateText(value, language) {
  const text = normalizeText(value);
  if (!text) return value;
  if (language === "en") return esToEn[text] || value;
  return enToEs[text] || value;
}

const I18nContext = createContext({
  language: "es",
  setLanguage: () => {},
  t: (value) => value,
});

export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState(() => {
    const saved = localStorage.getItem(LANGUAGE_KEY);
    return LANGUAGES[saved] ? saved : "es";
  });

  const setLanguage = useCallback((nextLanguage) => {
    const normalized = LANGUAGES[nextLanguage] ? nextLanguage : "es";
    localStorage.setItem(LANGUAGE_KEY, normalized);
    document.documentElement.lang = normalized;
    setLanguageState(normalized);
  }, []);

  useEffect(() => {
    document.documentElement.lang = language;
  }, [language]);

  const t = useCallback((value) => translateText(value, language), [language]);

  const value = useMemo(() => ({ language, setLanguage, t }), [language, setLanguage, t]);

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
